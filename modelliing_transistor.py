#уравнения Кирхгофа:

b=(1,2,3) #задаём вектор коэффициентов
w=(1,2) #задаём вектор значений источников питания

def kirh(y):
    global b

    return [y[1]+y[2]-y[3],
            y[1]*b[1]-y[2]*b[2]-w[1]-w[2],
            y[2]*b[2]+y[3]*b[3]+w[2]
            ]


